from typing import List

class Solution:
    def uniquePathsWithObstacles(self, obstacleGrid: List[List[int]]) -> int:
        def helper(grid, dp, m, n):
            if m < 0 or n < 0 or obstacleGrid[m][n] == 1:
                return 0
            if m == 0 and n == 0:
                return 1
            # print(f'{m}, {n}')
            if dp[m][n] == -1:
                dp[m][n] = helper(grid, dp, m - 1, n) + helper(grid, dp, m, n - 1)
            return dp[m][n]

        R = len(obstacleGrid)
        C = len(obstacleGrid[0])
        dp = [[-1 for _ in range(C)] for _ in range(R)]
        res = helper(obstacleGrid, dp, R - 1, C - 1)
        # print(f'{res}')
        return res

